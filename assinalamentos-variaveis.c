#include <stdio.h>

void main(void)
{
	// assinalamento
	int i, j , k, w;

	// atribuição multipla
	i = j = k = 20;

	printf("Variavel i: %d\n",i);
	printf("Variavel j: %d\n",j);
	printf("Variavel k: %d\n",k);

	// atribui 90 a w e imprime o seu conteúdo
	printf("Resultado da expressão: %d\n", w = 90);
	printf("Variavel w: %d\n", w);
}